CREATE TABLE panelists (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL DEFAULT '',
    team VARCHAR(100) NOT NULL DEFAULT ''
);

CREATE TABLE questions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    period INTEGER DEFAULT 0,
    idx INTEGER DEFAULT 0,
    panelist_type VARCHAR(100) DEFAULT '',
    format VARCHAR(100) DEFAULT '',
    text VARCHAR(100) DEFAULT '',
    file_name VARCHAR(100) DEFAULT '',
    thinking_second INTEGER DEFAULT 0,
    answer VARCHAR(1) DEFAULT '',
    point INTEGER DEFAULT 0
);

CREATE TABLE options (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    question_id INTEGER DEFAULT 0,
    text VARCHAR(100) DEFAULT '',
    image_origin_x FLOAT NOT NULL DEFAULT 0,
    image_origin_y FLOAT NOT NULL DEFAULT 0,
    image_scale FLOAT NOT NULL DEFAULT 0
);

CREATE TABLE answers (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    panelist_id INTEGER DEFAULT 0,
    question_id INTEGER  DEFAULT 0,
    answer VARCHAR(1) NOT NULL DEFAULT '',
    correct INTEGER NOT NULL DEFAULT 0,
    elapsed_second FLOAT NOT NULL DEFAULT 0
);
